package day27_WrapperClasses;

public class ClassNotes {

    /*
    Data Structures:
    1. Array: supports primitives and non primitives
    2. Collection: only support non primitives
    3. Map: only support non primitives
     */


    /*
    wrapper classes

    * dedicated classes of eight primitives
    * presented in "java.lang" package
    * used to "wrap" the primitive data type into an object of that class
    * provide the mechanism to convert primitive into object and object into primitive
     */

    /*
    primitive                                          wrapper classes

    byte                       |                        Byte
    short                      |                        Short
    int                        |                        Integer
    long                       |                        Long
    float                      |                        Float
    double                     |                        Double
    char                       |                        Character
    boolean                    |                        Boolean
     */


    /*
    Autoboxing vs unboxing
    * autoboxing converts primitive into wrapper class object
    * unboxing converts wrapper class object into primitive

                 auto boxing
    primitive===============>>>wrapper class

                  unboxing
    primitive<<<===============wrapper class
     */

    /*
    Wrapper classes:

    1. parse methods//ayristirma yontemleri
    //converts String to primitive
    "1234567"
    2. valueOf methods  // yontemlerin degeri
    //converts String to wrapper class
     */
}
