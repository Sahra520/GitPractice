package ImportantRules;

public class CharRules {
        public static void main(String[] args) {
            char ch1 = 105; //i
            char ch2 = 110; //n
            char ch3 = 97; //a
            char ch4 = 112; //p
            char ch5 = 108; //l
            char ch6 = 101; //e
            System.out.println("\t\t"+ch4+ch1+ch2+ch6+ch3+ch4+ch4+ch5+ch6+"?"); //output:pineapple
            System.out.println(ch4+ch1+ch2+ch6+ch3+ch4+ch4+ch5+ch6); //output: 958
            System.out.println("\t\t"+ch4+ch1+ch2+ch6+ch3+ch4+ch4+ch5+ch6); //pineapple
            System.out.println(ch4+ch1+ch2+ch6+ch3+ch4+ch4+ch5+ch6+"?"); //958?
            System.out.println("\t\t"+ch4+ch1+ch2+ch6+ch3+ch4+ch4+ch5+ch6+"?");
        }
    }

