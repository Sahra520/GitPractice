package day04_Variables;

public class Currencies {
    public static void main(String[] args) {
        int dollar = 1000;
        double YTL = dollar * 18.22;
        double euro = dollar * 0.99;
        double riyal = dollar * 3.76;
        double forint = dollar * 392.11;
        double canadianDollar = dollar * 1.30;

        System.out.println("YTL = " + YTL);
        System.out.println("euro = " + euro);
        System.out.println("riyal = " + riyal);
        System.out.println("forint = " + forint);
        System.out.println("canadianDollar = " + canadianDollar);



    }
}
